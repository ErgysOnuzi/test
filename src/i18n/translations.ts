export type TranslationKey = keyof typeof translations.de;
export type Locale = keyof typeof translations;

/**
 * Get translation for a given key and locale
 */
export function getTranslation(locale: Locale, keyPath: string): string {
  const keys = keyPath.split('.');
  let current: any = translations[locale] || translations.de;
  
  for (const key of keys) {
    current = current[key];
    if (current === undefined) {
      // Fallback to German translation
      let fallback: any = translations.de;
      for (const fallbackKey of keys) {
        fallback = fallback[fallbackKey];
        if (fallback === undefined) {
          return keyPath; // Return key path as fallback
        }
      }
      return fallback;
    }
  }
  
  return current || keyPath;
}

/**
 * Translation hook for React components
 */
export function useTranslation(locale: Locale) {
  return {
    t: (key: string) => getTranslation(locale, key),
    locale,
  };
}